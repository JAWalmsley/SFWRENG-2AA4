package ca.mcmaster.cas.se2aa4.a2.generator;

public class Vertex {
    private float x;
    private float y;

    public Vertex(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return this.x;
    }

    public float getY() {
        return this.y;
    }
}